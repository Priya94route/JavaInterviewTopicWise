package Java8;

import java.util.List;

class Employee {

int id;

String name;

int age;

double salary;

String department;

List<Project> projects;

public Employee(int id, String name, int age, double salary, String department, List<Project> projects) {
	super();
	this.id = id;
	this.name = name;
	this.age = age;
	this.salary = salary;
	this.department = department;
	this.projects = projects;
}



}